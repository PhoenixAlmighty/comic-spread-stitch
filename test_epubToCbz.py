# this function was written because filecmp.cmp() could get awfully picky about whether .zip files counted as equal
# so this function checks only the name lists and the underlying files, which is all I really care about
def checkZipFilesEqual(first, second):
	with ZipFile(first, 'r') as firstZip, ZipFile(second, 'r') as secondZip:
		fNameList = firstZip.namelist()
		sNameList = secondZip.namelist()
		if not fNameList == sNameList:
			return False
		return all([firstZip.read(name) == secondZip.read(name) for name in fNameList])


class TestConvertEpubToCbz(unittest.TestCase):
	def setUp(self):
		self.bookDir = os.path.join(os.path.dirname(__file__), "test-resources", "epub")
		os.chdir(self.bookDir)
		self.bookFile = "Test ePub.epub"

	# basic sanity
	def test_convertEpubToCbz_sanity(self):
		result, reason = epubToCbz.convertEpubToCbz(os.path.join(self.bookDir, self.bookFile))
		os.chdir("..")
		self.assertTrue(os.path.isfile(os.path.join("epub", f"{os.path.splitext(self.bookFile)[0]}.cbz")),
						f"{os.path.splitext(self.bookFile)[0]}.cbz should exist")
		self.assertTrue(checkZipFilesEqual(os.path.join("epub", f"{os.path.splitext(self.bookFile)[0]}.cbz"),
									os.path.join("cbz-from-epub", "Test ePub.cbz")),
						"File generated by convertEpubToCbz should be equal to the one in cbz-from-epub directory")
		self.assertEqual(result, 0, "Result should be 0")
		self.assertEqual(reason, f"{self.bookFile} converted to CBZ.",
						 f'Reason should be "{self.bookFile} converted to CBZ."')

	# book is not an ePub
	def test_convertEpubToCbz_notEpub(self):
		book = os.path.join(os.path.split(self.bookDir)[0], "cbz", "Test.cbz")
		result, reason = epubToCbz.convertEpubToCbz(book)
		self.assertEqual(result, 1, "Result should be 1")
		self.assertEqual(reason, f"{book} is not an ePub.")

	def tearDown(self):
		os.chdir(self.bookDir)
		if os.path.isfile(f"{os.path.splitext(self.bookFile)[0]}.cbz"):
			os.remove(f"{os.path.splitext(self.bookFile)[0]}.cbz")


