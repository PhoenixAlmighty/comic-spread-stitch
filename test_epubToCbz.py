class TestBuildCbzFile(unittest.TestCase):
	# basic sanity
	def test_buildCbzFile_sanity(self):
		imgDir = os.path.join(os.path.dirname(__file__), "test-resources")
		os.chdir(imgDir)
		imgs = ["baboon.png", "baboonccw.png", "babooncw.png", "boat.png", "boatccw.png", "boatcw.png"]
		imgs = [os.path.join("extracted-epub", "OEBPS", "images", i) for i in imgs]
		epubToCbz.buildCbzFile(imgs, "", "From ePub.cbz")
		self.assertTrue(checkZipFilesEqual("From ePub.cbz", os.path.join("cbz-from-buildfile", "From buildfile.cbz")),
						"File generated by buildCbzFile should be equal to the one in cbz-from-buildfile directory")

	# negative test
	# this should fail because the filenames in the zip archive should be different
	# even though the images are the same and in the same order
	def test_buildCbzFile_negative(self):
		imgDir = os.path.join(os.path.dirname(__file__), "test-resources")
		os.chdir(imgDir)
		imgs = ["baboon.png", "baboonccw.png", "babooncw.png", "boat.png", "boatccw.png", "boatcw.png"]
		imgs = [os.path.join("extracted-epub", "OEBPS", "images", i) for i in imgs]
		epubToCbz.buildCbzFile(imgs, "", "From ePub.cbz")
		self.assertFalse(
			checkZipFilesEqual("From ePub.cbz", os.path.join("cbz", "Test.cbz")),
			"File generated by buildCbzFile should not be equal to the one in cbz directory")

	def tearDown(self):
		os.chdir(os.path.join(os.path.dirname(__file__), "test-resources"))
		if os.path.isfile(os.path.join("From ePub.cbz")):
			os.remove(os.path.join("From ePub.cbz"))


